    private void jButtonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastroActionPerformed
        String[] dadosCliente = new String[5];
        dadosCliente[0] = TextNome.getText();
        dadosCliente[1] = TextSobrenome.getText();
        dadosCliente[2] = TextCpf.getText();
        dadosCliente[3] = TextIdade.getText();
        dadosCliente[4] = TextCep.getText();
        if(dadosValidar(TextNome.getText(), TextSobrenome.getText(), TextCpf.getText(), TextIdade.getText(), TextCep.getText())){
            if(iclientedao.cadastrar(dadosCliente)){
                Long cpf = Long.parseLong(TextCpf.getText());
                Cliente clienteCadastrado = iclientedao.consultar(cpf);
                limpar();
                modelTabela.addRow(new Object[]{clienteCadastrado.getNome(), clienteCadastrado.getSobrenome(), clienteCadastrado.getCpf(), clienteCadastrado.getIdade(), clienteCadastrado.getCep()});
               JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso!");
           }else {
               JOptionPane.showMessageDialog(null, "Cliente já Cadastrado!");
           }
        } else {
          JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
    }//GEN-LAST:event_jButtonCadastroActionPerformed

