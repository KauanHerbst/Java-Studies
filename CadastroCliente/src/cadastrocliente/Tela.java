    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
        int row = Tabela.getSelectedRow();
        TextCpf.setEditable(false);
        Long cpf = (Long) Tabela.getValueAt(row, 2);
        
        Cliente clienteCadastrado = iclientedao.consultar(cpf);
        TextNome.setText(clienteCadastrado.getNome());
        TextSobrenome.setText(clienteCadastrado.getSobrenome());
        TextCpf.setText(clienteCadastrado.getCpf().toString());
        TextIdade.setText(clienteCadastrado.getIdade().toString());
        TextCep.setText(clienteCadastrado.getCep().toString());
    }//GEN-LAST:event_TabelaMouseClicked

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int row = Tabela.getSelectedRow();
        
        if(row >= 0){
            int option = JOptionPane.showConfirmDialog(null, "Deseja Excluir esse cadastro?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(option == JOptionPane.YES_OPTION){
                Long cpf = (Long )Tabela.getValueAt(row, 2);
                iclientedao.excluir(cpf);
                limpar();
                JOptionPane.showMessageDialog(null, "Cliente Excluido!");
                modelTabela.removeRow(row);
            }else {
                limpar();
            }
        }else {
            JOptionPane.showMessageDialog(null, "Nenhum Cliente Selecionado!");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        int row = Tabela.getSelectedRow();
        
        if(row >= 0){
            int option = JOptionPane.showConfirmDialog(null, "Deseja atualizar esse cadastro?", "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(option == JOptionPane.YES_OPTION){
                Long cpf = (Long )Tabela.getValueAt(row, 2);
                Cliente clienteCadastrado = iclientedao.consultar(cpf);
                clienteCadastrado.setNome(TextNome.getText());
                clienteCadastrado.setSobrenome(TextSobrenome.getText());
                clienteCadastrado.setIdade(Integer.parseInt(TextIdade.getText()));
                clienteCadastrado.setCep(Long.parseLong(TextCep.getText()));
                JOptionPane.showMessageDialog(null, "Cliente Atualizado!");
                modelTabela.removeRow(row);
                modelTabela.addRow(new Object[]{clienteCadastrado.getNome(), clienteCadastrado.getSobrenome(), clienteCadastrado.getCpf(), clienteCadastrado.getIdade(), clienteCadastrado.getCep()});
                limpar();
                TextCpf.setEditable(true);
            }else {
                limpar();
            }
        }else {
            JOptionPane.showMessageDialog(null, "Nenhum Cliente Selecionado!");
        }
        
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

